DATA SEGMENT USE16
	NUM DW 0000H 
DATA ENDS
CODE SEGMENT USE16
ASSUME CS:CODE,DS:DATA
MOV AX,DATA
MOV DS,AX
;BCD码表示
BEG: MOV CX,4 ;4次调用
MOV AX,0
AGA: ROL NUM,4
MOV BX,NUM
AND BX,000FH
IMUL AX,10
ADD AX,BX
LOOP AGA
CALL DISP
DISP PROC
		MOV BX,AX
		MOV CX,16
LAST1:	MOV AL,'0'
		ROL BX,1
		JNC NEXT
		MOV AL,'1'
NEXT:	MOV AH,0EH
		INT 10H
		LOOP LAST1
DISP ENDP
MOV AH,4CH
INT 21H
CODE ENDS
END BEG




