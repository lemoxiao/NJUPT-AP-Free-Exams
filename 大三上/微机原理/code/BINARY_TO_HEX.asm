DATA SEGMENT USE16
		BUF DW 987AH
DATA ENDS
CODE SEGMENT USE16
ASSUME CS:CODE,DS:DATA
BEG: 	MOV AX,DATA
		MOV DS,AX
		MOV DX,BUF
		MOV CX,4
		SAL EDX,16
		AGA: ROL EDX,4
		AND DL,0FH
		; 截取四位二进制数
		CMP DL,10
		;判断范围,转换为ASCII码
		JC NEXT
		ADD DL,7
NEXT: 	ADD DL,30H
		MOV AH,2
		INT 21H
		;显示字符
		LOOP AGA
		MOV AH,4CH
		INT 21H
CODE ENDS
END BEG

